    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        // TODO add your handling code here:
        String Email = studentemailid.getText().toUpperCase();
        String pwd = new String(password.getPassword());
        String query = "SELECT * FROM STUDENTS WHERE STUDENT_EMAIL='"+Email+"'";
                
        Conn conn = new Conn();
        
        try{
            ResultSet results = conn.s.executeQuery(query);
            int rollno = 0;
            if(results.next()){
               if(!results.getString(10).toUpperCase().equals(Email) || !results.getString(11).equals(pwd+securitykey)){
                   JOptionPane.showMessageDialog(this, "Invalid Email or Password Try Again!!!!");
               }
               else{
                   rollno = Integer.parseInt(results.getString(1));
                   JOptionPane.showMessageDialog(this, "Congratulations!!! Login Successfull.....");
                   studentDashboardUI dashboard = new studentDashboardUI(rollno);
                   dashboard.setVisible(true);
                   setVisible(false);
               }
            }
            else if(!results.next()){
                JOptionPane.showMessageDialog(null, "Account not Registered Kindly Register Yourself....");
                setVisible(false);
                StudentloginUI studentlogin = new StudentloginUI();
                studentlogin.setVisible(true);
            }
        }
        catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_loginActionPerformed

