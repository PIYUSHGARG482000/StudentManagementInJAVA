    private void studentemailidActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_studentemailidActionPerformed
        String regex = "^[A-Za-z0-9+_.-]+@(.+)$";  
        Pattern pattern = Pattern.compile(regex); 
        Matcher matcher = pattern.matcher(studentemailid.getText());
        if(!matcher.matches()){
            JOptionPane.showMessageDialog(this,"Please Enter a Valid Email ID.");
            setVisible(true);
        }
        else if(studentemailid.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Please Enter Email ID to Login.");
            setVisible(true);
        }
    }//GEN-LAST:event_studentemailidActionPerformed

