    private void RollNoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RollNoActionPerformed
        // TODO add your handling code here:
        String PATTERN="^[0-9].{3,6}$";
        Pattern patt=Pattern.compile(PATTERN);
        Matcher match=patt.matcher(RollNo.getText());
        if(!match.matches()){
            nRoll.setText("Invalid Roll No!!!");
        }
        else{
            nRoll.setText(null);
        }
    }//GEN-LAST:event_RollNoActionPerformed

    private void emialIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_emialIdActionPerformed
        // TODO add your handling code here:
        String PATTERN="^[a-zA-Z0-9]{0,30}[@][a-zA-Z0-9]{0,10}[.][a-zA-Z]{0,5}$";
        Pattern patt=Pattern.compile(PATTERN);
        Matcher match=patt.matcher(emialId.getText());
        if(!match.matches()){
            nEmail.setText("Invalid email id!!!");
        }
        else{
            nEmail.setText(null);
        }
    }//GEN-LAST:event_emialIdActionPerformed

