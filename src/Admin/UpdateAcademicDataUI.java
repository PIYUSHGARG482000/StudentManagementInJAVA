    public char computegrade(int percentage) {
        if (percentage >= 80&& percentage < 100) {
            return 'A';
        }else if (percentage >= 60 && percentage < 80) {
            return 'B';
        }  else if (percentage >= 40 && percentage < 60) {
            return 'C'; 
        }
            return 'D';
    }

