    private void SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveActionPerformed
        // TODO add your handling code here:
        int rollno = Integer.parseInt(RollNo.getText());
        String email = emialId.getText();
        int obtained = Integer.parseInt(obtainedMarks.getText());
        int per = (obtained*100)/500;
        char grade = computegrade(per);
        Conn conn;
        try{
            String query = "UPDATE STUDENTS SET MARKS='"+obtained+"', PERCENTAGE='"+per+"', GRADE='"+grade+"' WHERE ROLLNO='"+rollno+"' AND STUDENT_EMAIL='"+email+"'";
            conn = new Conn();
            int response = conn.s.executeUpdate(query);
            if(response > 0){
                JOptionPane.showMessageDialog(null, "Data Saved Successfully");
            }
        }
        catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_SaveActionPerformed

